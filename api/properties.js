function handler(req, res) {
  // CORS headers
  res.setHeader('Access-Control-Allow-Credentials', 'true');
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET,OPTIONS,POST');
  res.setHeader('Access-Control-Allow-Headers', 'X-CSRF-Token,X-Requested-With,Accept,Accept-Version,Content-Length,Content-MD5,Content-Type,Date,X-Api-Version');

  if (req.method === 'OPTIONS') {
    res.status(200).end();
    return;
  }

  const disclaimer = '*Valores sujeito a alteraÃ§Ã£o pela incorporadora. Ref: 03/2026';

  if (req.method === 'GET') {
    if (req.query.recommend) {
      const preferences = req.query.preferences ? JSON.parse(req.query.preferences) : req.query;
      const recommendations = recommendProperties(preferences);
      return res.status(200).json({
        success: true,
        count: recommendations.length,
        data: recommendations,
        disclaimer
      });
    }

    const filtered = filterProperties(req.query);
    return res.status(200).json({
      success: true,
      count: filtered.length,
      total_available: properties.length,
      data: filtered,
      disclaimer
    });
  }

  if (req.method === 'POST') {
    if (req.body.action === 'search') {
      const filtered = filterProperties(req.body.filters || {});
      return res.status(200).json({
        success: true,
        count: filtered.length,
        data: filtered,
        disclaimer
      });
    }

    if (req.body.action === 'recommend') {
      const recommendations = recommendProperties(req.body.preferences);
      return res.status(200).json({
        success: true,
        count: recommendations.length,
        data: recommendations,
        disclaimer
      });
    }
  }

  res.status(405).json({ error: 'Method not allowed' });
}
