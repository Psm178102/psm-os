function filterProperties(query) {
  let filtered = properties;

  if (query.regiao) {
    filtered = filtered.filter(p => p.regiao?.toUpperCase() === query.regiao.toUpperCase());
  }

  if (query.dorms !== undefined) {
    const dorms = parseInt(query.dorms);
    filtered = filtered.filter(p => p.dorms === dorms);
  }

  if (query.valor_min !== undefined) {
    const min = parseInt(query.valor_min);
    filtered = filtered.filter(p => p.valor >= min);
  }

  if (query.valor_max !== undefined) {
    const max = parseInt(query.valor_max);
    filtered = filtered.filter(p => p.valor <= max);
  }

  if (query.categoria) {
    filtered = filtered.filter(p => p.categoria === query.categoria.toLowerCase());
  }

  if (query.tipo) {
    filtered = filtered.filter(p => p.tipo === query.tipo.toLowerCase());
  }

  if (query.m2_min !== undefined) {
    const min = parseFloat(query.m2_min);
    filtered = filtered.filter(p => p.m2 && p.m2 >= min);
  }

  if (query.m2_max !== undefined) {
    const max = parseFloat(query.m2_max);
    filtered = filtered.filter(p => p.m2 && p.m2 <= max);
  }

  if (query.search) {
    const term = query.search.toLowerCase();
    filtered = filtered.filter(p =>
      p.nome.toLowerCase().includes(term) ||
      p.incorporadora.toLowerCase().includes(term)
    );
  }

  return filtered;
}

function recommendProperties(preferences) {
  let scored = properties.map(prop => {
    let score = 0;

    if (preferences.regiao && prop.regiao?.toUpperCase() === preferences.regiao.toUpperCase()) {
      score += 100;
    }

    if (preferences.dorms && prop.dorms === preferences.dorms) {
      score += 50;
    }

    if (preferences.budget_max && prop.valor <= preferences.budget_max) {
      score += 30;
    }

    if (preferences.budget_min && prop.valor >= preferences.budget_min) {
      score += 30;
    }

    if (preferences.categoria && prop.categoria === preferences.categoria) {
      score += 40;
    }

    if (preferences.tipo && prop.tipo === preferences.tipo) {
      score += 20;
    }

    if (preferences.vagas && prop.vagas && parseInt(prop.vagas) >= preferences.vagas) {
      score += 15;
    }

    return { ...prop, score };
  });

  return scored
    .filter(p => p.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 5)
    .map(({ score, ...p }) => p);
}

